import displayio
import adafruit_imageload


class PacmanMap:
    '''Pacman map class with sprite sheet

       Tile: 128 x 160
    '''

    def __init__(self, sprite_sheet: str):
        self.bitmap, palette = adafruit_imageload.load(
            sprite_sheet,
            bitmap=displayio.Bitmap,
            palette=displayio.Palette
        )

        self.sprite = displayio.TileGrid(
            self.bitmap,
            pixel_shader=palette,
        )


